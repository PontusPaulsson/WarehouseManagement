package warehouseproject.Utility;

public class Adress {
    
    private String streetName;
    private String zipCode;
    private String city;
    private String phonenNumber;

    public Adress(String streetName, String zipCode, String city, String phonenNumber) {
        this.streetName = streetName;
        this.zipCode = zipCode;
        this.city = city;
        this.phonenNumber = phonenNumber;
    }

}
